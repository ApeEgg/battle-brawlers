<script lang="ts">
  import { SETTINGS_DEFAULT_VOLUME } from '$src/app.svelte';

  let { onclick } = $props();

  const prettyVolume = (value: number) => {
    return `${(value * 100) | 0}%`;
  };
</script>

{#snippet displayMasterVolume()}
  <crow class="w-10 p-2 text-sm">{prettyVolume(app.settings.volume.master)}</crow>
{/snippet}
{#snippet displayMusicVolume()}
  <crow class="w-10 p-2 text-sm">{prettyVolume(app.settings.volume.music)}</crow>
{/snippet}
{#snippet displaySFXVolume()}
  <crow class="w-10 p-2 text-sm">{prettyVolume(app.settings.volume.sfx)}</crow>
{/snippet}

<Frame title="options">
  <crow left class="w-full !justify-between text-white">
    <span class="cinzel">Audio settings</span>

    <Button
      secondary
      disabled={JSON.stringify(app.settings.volume) === JSON.stringify(SETTINGS_DEFAULT_VOLUME)}
      onclick={() => {
        app.settings.volume = SETTINGS_DEFAULT_VOLUME;
      }}
    >
      Reset to default
    </Button>
  </crow>
  <crow class="w-full gap-4">
    <div class="w-40">Master</div>
    <!-- <div class="w-14 text-xs">
      {(app.settings.volume.master * 100) | 0}%
    </div> -->

    <input
      use:tooltip={{
        children: displayMasterVolume,
        direction: 'right',
        lockInPlace: true
      }}
      type="range"
      min="0"
      max="1"
      step="0.1"
      bind:value={app.settings.volume.master}
      class="w-full"
    />
  </crow>
  <crow class="w-full gap-4">
    <div class="w-40">Music</div>
    <!-- <div class="w-14 text-xs">
      {(app.settings.volume.music * 100) | 0}%
    </div> -->
    <input
      use:tooltip={{
        children: displayMusicVolume,
        direction: 'right',
        lockInPlace: true
      }}
      type="range"
      min="0"
      max="1"
      step="0.1"
      bind:value={app.settings.volume.music}
      class="w-full"
    />
  </crow>
  <crow class="w-full gap-4">
    <div class="w-40">Sound effect</div>
    <!-- <div class="w-14 text-xs">
      {(app.settings.volume.sfx * 100) | 0}%
    </div> -->
    <input
      use:tooltip={{
        children: displaySFXVolume,
        direction: 'right',
        lockInPlace: true
      }}
      type="range"
      min="0"
      max="1"
      step="0.1"
      bind:value={app.settings.volume.sfx}
      class="w-full"
    />
  </crow>

  <Hr class="via-white/20" />

  <Button secondary onclick={() => (app.overlay = 'ReleaseNotes')}>Release notes</Button>
  {#if app.token}
    <Logout />
  {/if}
</Frame>

<Close {onclick} />
